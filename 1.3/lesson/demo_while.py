# Пример №2 (Сумма чисел от 1 до 100 кратных 3)
i = 1 #счетчик цикла
s = 0 #результат суммирования
while i <= 100:
    if i % 3 == 0:
        print(i)
        s = s + i
    i += 1

print(s)
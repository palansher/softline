# Цикл - это конструкция, которая позволяет избежать дублирования кода за счет повторения действий.
# Основные термины:
# 1) Счетчик цикла - это переменная от которой зависит количество повторений тела цикла
# 2) Тело цикла - это инструкции, которые выполняются при каждой итерации цикла
# 3) Итерация цикла - это количество повторений выполнений тела цикла

# Цикл с предусловием - это цикл while

# while условие:
#     тело цикла

# Тело цикла выполняется до тех пор, пока условие в while истино, то есть не равно False



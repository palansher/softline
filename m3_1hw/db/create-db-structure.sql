CREATE OR REPLACE VIEW v_catalog_display AS
SELECT
    c.id AS car_id,
    b.name AS brand_name,
    c.model AS model_name,
    c.short_info,
    c.full_info,
    c.price,
    c.year_produced,
    c.mileage,
    c.image_small_path,
    c.image_big_path
FROM
    catalog AS c
INNER JOIN brand AS b ON c.brand_id = b.id
WHERE
    c.is_available = TRUE;


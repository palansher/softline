"""Контекстные процессоры для корзины."""

from .cart import Cart


def cart(request):
    """Добавить текущую корзину в контекст всех шаблонов.

    Позволяет обращаться к ``{{ cart }}`` из любого шаблона
    для отображения количества товаров в шапке сайта.
    """
    return {'cart': Cart(request)}

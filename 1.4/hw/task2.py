"""
С клавиатуры вводятся N чисел. Составьте программу, которая
определяет количество отрицательных, количество положительных
и количество нулей среди введенных чисел.
Значение N вводится с клавиатуры. При вводе нечислового значения вывести сообщение
об ошибке и просим ввести повторно именно числовое значение.
Используйте цикл for.
"""

# Ввод N (только положительное целое - сколько чисел введем)
n_str = input("Введите количество чисел N: ")
while not n_str.isdigit():
    print("Ошибка: введите целое положительное число.")
    n_str = input("Введите количество чисел N: ")

n = int(n_str)

pos = neg = zero = 0

# Основной цикл для ввода N чисел
for i in range(n):
    # Проверка каждого числа (может быть отрицательным)
    valid = False
    while not valid:
        num_str = input(f"Введите целое число {i + 1}: ")

        # Проверяем введенные числа на integer, даже если они отрицательные
        # Убираем минус один раз слева и проверяем, остались ли только цифры
        if num_str.lstrip('-').isdigit() and num_str != '-':
            num = int(num_str)
            valid = True
        else:
            print("Ошибка: введите именно целое числовое значение.")

    # Распределение по категориям
    if num > 0:
        pos += 1
    elif num < 0:
        neg += 1
    else:
        zero += 1

# 4. Итоговый вывод
print(f"\nПоложительных: {pos}")
print(f"Отрицательных: {neg}")
print(f"Нулей: {zero}")

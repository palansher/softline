"""
Здесь мы объявляем абстрактный класс (интерфейс) для клиента
и сам класс-заместитель. 
Для проверки через eval перед отправкой используем регулярное выражение, 
чтобы убедиться, что строка содержит только математические символы и цифры
"""


from abc import ABC, abstractmethod
import re


class MathClientInterface(ABC):
    """
    Абстрактный интерфейс для клиента, запрашивающего математические вычисления.
    Определяет требования к методам для реального клиента и прокси.
    """

    @abstractmethod
    def send_expression(self, expression: str) -> str:
        """Отправляет выражение на обработку и возвращает результат."""
        pass


class ClientProxy(MathClientInterface):
    """
    Прокси-класс (Заместитель). Проверяет корректность математического
    выражения до того, как оно будет отправлено на сервер по сети.
    """

    def __init__(self, real_client: MathClientInterface) -> None:
        """
        Инициализация прокси.

        :param real_client: Экземпляр реального сетевого клиента.
        """
        self._real_client: MathClientInterface = real_client
        """
        Разрешаем только цифры, знаки операций, точки, скобки и пробелы
        
        Python берет эту текстовую строку с шаблоном и превращает (компилирует) её
        в специальный высокоэффективный объект регулярного выражения.
        
        compile - это для производительности при дальнейшем использовании regex выражения.
        Python один раз при старте программы превращает строку в готовый бинарный шаблон.
        """
        self._valid_chars = re.compile(r"^[0-9+\-*/().\s]+$")

    def _is_valid(self, expression: str) -> bool:
        """
        Внутренний метод для валидации строки выражения.
        
        Эта строчка проверяет, не является ли введенная строка пустой или 
        состоящей только из пробелов. 
        Она защищает программу от падения, если пользователь случайно нажал Enter, 
        ничего не введя, или просто наставил пробелов.
        """
        if not expression.strip():
            return False

        # 1. Проверка на разрешенные символы (безопасность eval)
        if not self._valid_chars.match(expression):
            return False

        # 2. Быстрая проверка синтаксиса через локальный eval
        try:
            # compile в режиме 'eval' проверит синтаксис без вычисления
            """
            expression — сама строка мат. выражения (например, "2 + 2 * 3" или каша +++777...).
            
            '<string>' — просто техническое имя для логов (если упадет ошибка).
            Поскольку мы компилируем строку из памяти, а не файл с диска, здесь пишется стандартное заглушка-имя.

            'eval' Заставляет Python проверять строку именно как одиночное математическое или логическое выражение, которое должно возвращать результат.
            
            Если строка написана по правилам математики (например, "2 + (3 * 4)"), 
            то compile() успешно создаст байт-код и молча пойдет дальше.
            Метод вернет True.

            Если в строке нарушен синтаксис,
            то compile() споткнется и вызовет исключение SyntaxError.
            """            
            compile(expression, "<string>", "eval")
            return True
        except (SyntaxError, TypeError):
            return False

    def send_expression(self, expression: str) -> str:
        """
        Перехватывает запрос, валидирует его, и если всё ок —
        передает реальному клиенту для отправки на сервер.
        """
        if not self._is_valid(expression):
            return "Ошибка [Proxy]: Некорректное или опасное математическое выражение!"

        # Если проверка пройдена, делегируем работу реальному клиенту
        return self._real_client.send_expression(expression)
